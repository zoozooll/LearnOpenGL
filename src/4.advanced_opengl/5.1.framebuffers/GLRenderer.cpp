#include "GLRenderer.h"

GLRenderer::GLRenderer(unsigned int width, unsigned int height)
    : shader(nullptr),
      screenShader(nullptr),
      camera(nullptr),
      cubeTexture(0),
      floorTexture(0),
      textureColorbuffer(0),
      cubeVAO(0), cubeVBO(0),
      planeVAO(0), planeVBO(0),
      quadVAO(0), quadVBO(0),
      framebuffer(0),
      rbo(0),
      screenWidth(width),
      screenHeight(height)
{}

GLRenderer::~GLRenderer()
{
    // Cleanup resources
    if (shader) {
        delete shader;
    }
    if (screenShader) {
        delete screenShader;
    }
    if (camera) {
        delete camera;
    }
    
    // Delete textures
    glDeleteTextures(1, &cubeTexture);
    glDeleteTextures(1, &floorTexture);
    glDeleteTextures(1, &textureColorbuffer);
    
    // Delete VAOs and VBOs
    glDeleteVertexArrays(1, &cubeVAO);
    glDeleteVertexArrays(1, &planeVAO);
    glDeleteVertexArrays(1, &quadVAO);
    glDeleteBuffers(1, &cubeVBO);
    glDeleteBuffers(1, &planeVBO);
    glDeleteBuffers(1, &quadVBO);
    
    // Delete framebuffer and renderbuffer
    glDeleteRenderbuffers(1, &rbo);
    glDeleteFramebuffers(1, &framebuffer);
}

bool GLRenderer::initialize()
{
    // Create camera
    camera = new Camera(glm::vec3(0.0f, 0.0f, 3.0f));
    
    // Setup shaders
    if (!setupShaders()) {
        return false;
    }
    
    // Setup geometry
    if (!setupGeometry()) {
        return false;
    }
    
    // Setup textures
    if (!setupTextures()) {
        return false;
    }
    
    // Setup framebuffer
    if (!setupFramebuffer()) {
        return false;
    }
    
    // Configure global OpenGL state
    glEnable(GL_DEPTH_TEST);
    
    return true;
}

void GLRenderer::render(float deltaTime)
{
    // bind to framebuffer and draw scene as we normally would to color texture 
    glBindFramebuffer(GL_FRAMEBUFFER, framebuffer);
    glEnable(GL_DEPTH_TEST); // enable depth testing (is disabled for rendering screen-space quad)

    // make sure we clear the framebuffer's content
    glClearColor(0.1f, 0.1f, 0.1f, 1.0f);
    glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);

    shader->use();
    glm::mat4 model = glm::mat4(1.0f);
    glm::mat4 view = camera->GetViewMatrix();
    glm::mat4 projection = glm::perspective(glm::radians(camera->Zoom), (float)screenWidth / (float)screenHeight, 0.1f, 100.0f);
    shader->setMat4("view", view);
    shader->setMat4("projection", projection);
    
    // cubes
    glBindVertexArray(cubeVAO);
    glActiveTexture(GL_TEXTURE0);
    glBindTexture(GL_TEXTURE_2D, cubeTexture);
    model = glm::translate(model, glm::vec3(-1.0f, 0.0f, -1.0f));
    shader->setMat4("model", model);
    glDrawArrays(GL_TRIANGLES, 0, 36);
    
    model = glm::mat4(1.0f);
    model = glm::translate(model, glm::vec3(2.0f, 0.0f, 0.0f));
    shader->setMat4("model", model);
    glDrawArrays(GL_TRIANGLES, 0, 36);
    
    // floor
    glBindVertexArray(planeVAO);
    glBindTexture(GL_TEXTURE_2D, floorTexture);
    shader->setMat4("model", glm::mat4(1.0f));
    glDrawArrays(GL_TRIANGLES, 0, 6);
    
    glBindVertexArray(0);

    // now bind back to default framebuffer and draw a quad plane with the attached framebuffer color texture
    glBindFramebuffer(GL_FRAMEBUFFER, 0);
    glDisable(GL_DEPTH_TEST); // disable depth test so screen-space quad isn't discarded due to depth test.
    
    // clear all relevant buffers
    glClearColor(1.0f, 1.0f, 1.0f, 1.0f); // set clear color to white (not really necessary actually, since we won't be able to see behind the quad anyways)
    glClear(GL_COLOR_BUFFER_BIT);

    screenShader->use();
    glBindVertexArray(quadVAO);
    glBindTexture(GL_TEXTURE_2D, textureColorbuffer);	// use the color attachment texture as the texture of the quad plane
    glDrawArrays(GL_TRIANGLES, 0, 6);
    
    glBindVertexArray(0);
}

void GLRenderer::onSizeChanged(int width, int height)
{
    screenWidth = width;
    screenHeight = height;
    glViewport(0, 0, width, height);
    
    // Recreate framebuffer with new size
    setupFramebuffer();
}

Camera* GLRenderer::getCamera()
{
    return camera;
}

bool GLRenderer::setupShaders()
{
    try {
        shader = new Shader("5.1.framebuffers.vs", "5.1.framebuffers.fs");
        shader->use();
        shader->setInt("texture1", 0);
        
        screenShader = new Shader("5.1.framebuffers_screen.vs", "5.1.framebuffers_screen.fs");
        screenShader->use();
        screenShader->setInt("screenTexture", 0);
        
        return true;
    } catch (const std::exception& e) {
        std::cout << "Failed to load shaders: " << e.what() << std::endl;
        return false;
    }
}

